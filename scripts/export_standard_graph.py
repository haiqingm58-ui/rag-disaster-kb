def _build_summary_md(stats: dict) -> str:
    lines = [
        "# 导出摘要",
        f"\n导出时间: {stats['exported_at']}",
        f"\n## 统计",
        f"\n- 标准数量: {stats['standard_count']}",
        f"- 节点数量: {stats['node_count']}",
        f"- 关系数量: {stats['rel_count']}",
    ]
    if stats.get("standards"):
        lines.append("\n## 导出标准")
        lines.append("\n| 编号 | 标题 |")
        lines.append("|------|------|")
        for s in stats["standards"]:
            lines.append(f"| {s['code']} | {s['title']} |")

    lines.append("\n## 导出文件")
    for f in stats.get("files", []):
        lines.append(f"- {f}")

    lines.append("\n## 安全检查")
    lines.append("- ✅ 不含 API Key")
    lines.append("- ✅ 不含 Neo4j 密码")
    lines.append("- ✅ 不含 .env 配置")
    lines.append("- ✅ 绝对路径已脱敏为文件名")
    return "\n".join(lines)


def export_all(output_dir: Path, code: str = "") -> dict:
    """Main export function."""
    db = neo4j_config()[3]
    output_dir.mkdir(parents=True, exist_ok=True)

    with get_session(db) as sess:
        # Get standard list
        std_recs = list(sess.run(
            "MATCH (s:StandardDocument) "
            + (f"WHERE s.code = '{code}' " if code else "") +
            "RETURN s.code, s.title, s.standard_id ORDER BY s.code"
        ))
        standards = [{"code": r["s.code"], "title": r["s.title"]} for r in std_recs]

        # Export nodes
        nodes = _export_nodes(sess, code)
        rels = _export_relationships(sess, code)

    # Write files
    stats = {
        "exported_at": datetime.now(timezone.utc).isoformat(),
        "standard_count": len(standards),
        "node_count": len(nodes),
        "rel_count": len(rels),
        "standards": standards,
        "files": [],
    }

    # nodes.json
    (output_dir / "nodes.json").write_text(
        json.dumps(nodes, ensure_ascii=False, indent=2), encoding="utf-8")
    stats["files"].append("nodes.json")

    # relationships.json
    (output_dir / "relationships.json").write_text(
        json.dumps(rels, ensure_ascii=False, indent=2), encoding="utf-8")
    stats["files"].append("relationships.json")

    # graph.cypher
    cypher = _build_cypher(nodes, rels)
    (output_dir / "graph.cypher").write_text(cypher, encoding="utf-8")
    stats["files"].append("graph.cypher")

    # schema.md
    (output_dir / "schema.md").write_text(_build_schema_md(), encoding="utf-8")
    stats["files"].append("schema.md")

    # import_guide.md
    (output_dir / "import_guide.md").write_text(_build_import_guide_md(), encoding="utf-8")
    stats["files"].append("import_guide.md")

    # neo4j_queries.md (copy from existing)
    if QUERIES_MD_SRC.exists():
        shutil.copy(QUERIES_MD_SRC, output_dir / "neo4j_queries.md")
        stats["files"].append("neo4j_queries.md")

    # export_summary
    (output_dir / "export_summary.json").write_text(
        json.dumps(stats, ensure_ascii=False, indent=2), encoding="utf-8")
    stats["files"].append("export_summary.json")

    summary_md = _build_summary_md(stats)
    (output_dir / "export_summary.md").write_text(summary_md, encoding="utf-8")
    stats["files"].append("export_summary.md")

    # zip
    zip_path = output_dir.parent / f"{output_dir.name}.zip"
    shutil.make_archive(
        str(zip_path.with_suffix("")), "zip", output_dir.parent, output_dir.name,
    )
    stats["zip_file"] = str(zip_path)

    return stats


def main():
    ap = argparse.ArgumentParser(description="Export knowledge graph from Neo4j")
    ap.add_argument("--all", action="store_true", help="Export all standards")
    ap.add_argument("--code", default="", help="Export specific standard by code")
    ap.add_argument("--output-dir", default="exports/standard_kg_export",
                    help="Output directory (default: exports/standard_kg_export)")

    args = ap.parse_args()
    if not args.all and not args.code:
        ap.error("Must specify --all or --code")

    output_dir = Path(args.output_dir)
    stats = export_all(output_dir, code=args.code if not args.all else "")

    print(f"导出完成: {output_dir}")
    print(f"  标准: {stats['standard_count']}")
    print(f"  节点: {stats['node_count']}")
    print(f"  关系: {stats['rel_count']}")
    print(f"  ZIP:  {stats.get('zip_file', 'N/A')}")
    print(f"  文件: {', '.join(stats['files'])}")


if __name__ == "__main__":
    main()

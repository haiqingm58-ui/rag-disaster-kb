"""Export industry standard knowledge graph from Neo4j to shareable files.

Usage:
  python scripts/export_standard_graph.py --all
  python scripts/export_standard_graph.py --code "GB/T 32864-2016"
  python scripts/export_standard_graph.py --all --output-dir exports/my_export
"""

from __future__ import annotations

import argparse
import json
import os
import shutil
import sys
from datetime import datetime, timezone
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))

from src.graph.common.neo4j_client import get_session, neo4j_config

QUERIES_MD_SRC = Path("data/standards/import_reports/neo4j_queries.md")

NODE_QUERY = """
MATCH (n)
WHERE (n:StandardDocument OR n:Chapter OR n:Clause OR n:Term
       OR n:Requirement OR n:Indicator OR n:Method OR n:StandardObject)
  AND n.standard_id IS NOT NULL
{}
RETURN n
"""

REL_QUERY = """
MATCH (a)-[r]->(b)
WHERE (a:StandardDocument OR a:Chapter OR a:Clause OR a:Term
       OR a:Requirement OR a:Indicator OR a:Method OR a:StandardObject)
  AND (b:StandardDocument OR b:Chapter OR b:Clause OR b:Term
       OR b:Requirement OR b:Indicator OR b:Method OR b:StandardObject)
{}
RETURN a, r, b
"""


def _sanitize_path(value: str) -> str:
    """Strip local absolute paths to just the filename."""
    if not value:
        return value
    return Path(value).name


def _export_nodes(session, code_filter: str) -> list[dict]:
    where = ""
    params = {}
    if code_filter:
        where = "AND n.standard_id IN (SELECT s.standard_id FROM (MATCH (s:StandardDocument {code: $code}) RETURN s.standard_id))"
        params["code"] = code_filter

    result = []
    records = session.run(NODE_QUERY.format(where), params)
    for rec in records:
        node = rec["n"]
        props = dict(node)
        # Sanitize paths
        if "source_file" in props:
            props["source_file"] = _sanitize_path(props.get("source_file", ""))
        result.append({
            "id": node.element_id,
            "labels": list(node.labels),
            "properties": props,
        })
    return result


def _export_relationships(session, code_filter: str) -> list[dict]:
    where_a = ""
    where_b = ""
    params = {}
    if code_filter:
        where_a = "AND a.standard_id IN (SELECT s.standard_id FROM (MATCH (s:StandardDocument {code: $code}) RETURN s.standard_id))"
        where_b = "AND b.standard_id IN (SELECT s.standard_id FROM (MATCH (s:StandardDocument {code: $code}) RETURN s.standard_id))"
        params["code"] = code_filter

    result = []
    records = session.run(
        REL_QUERY.format(where_a).replace("AND (b:", where_b + " AND (b:"),
        params,
    )
    for rec in records:
        a, r, b = rec["a"], rec["r"], rec["b"]
        result.append({
            "id": r.element_id,
            "type": r.type,
            "start_node": a.element_id,
            "end_node": b.element_id,
            "properties": dict(r),
        })
    return result


def _build_cypher(nodes: list[dict], rels: list[dict]) -> str:
    lines = [
        "// Industry Standard Knowledge Graph — Auto-generated by export_standard_graph.py",
        f"// {len(nodes)} nodes, {len(rels)} relationships",
        "",
    ]

    # Nodes
    for node in nodes:
        labels = ":".join(node["labels"])
        props = node["properties"]
        # Use the stable unique ID field for MERGE
        # Unique ID per label type (ordered by specificity)
        label_set = set(node["labels"])
        if "StandardDocument" in label_set:
            uid_field, uid_value = "standard_id", props.pop("standard_id", None)
        elif "Chapter" in label_set:
            uid_field, uid_value = "chapter_id", props.pop("chapter_id", None)
        elif "Clause" in label_set:
            uid_field, uid_value = "clause_id", props.pop("clause_id", None)
        elif "Term" in label_set:
            uid_field, uid_value = "term_id", props.pop("term_id", None)
        elif "Requirement" in label_set:
            uid_field, uid_value = "requirement_id", props.pop("requirement_id", None)
        elif "Indicator" in label_set:
            uid_field, uid_value = "indicator_id", props.pop("indicator_id", None)
        elif "Method" in label_set:
            uid_field, uid_value = "method_id", props.pop("method_id", None)
        elif "StandardObject" in label_set:
            uid_field, uid_value = "object_id", props.pop("object_id", None)
        if not uid_field:
            continue

        set_parts = []
        for k, v in props.items():
            if v is None:
                continue
            if isinstance(v, (int, float)):
                set_parts.append(f"  n.{k} = {json.dumps(v)}")
            elif isinstance(v, bool):
                set_parts.append(f"  n.{k} = {str(v).lower()}")
            else:
                escaped = json.dumps(str(v), ensure_ascii=False)
                set_parts.append(f"  n.{k} = {escaped}")

        node_labels = ":".join(node["labels"])
        lines.append(f"MERGE (n:{node_labels} {{{uid_field}: {json.dumps(uid_value, ensure_ascii=False)}}})")
        if set_parts:
            lines.append("SET " + ",\n    ".join(set_parts))
        lines.append(";")
        lines.append("")

    # Relationships
    for rel in rels:
        rtype = rel["type"]
        # Find start/end node IDs
        start_node = next((n for n in nodes if n["id"] == rel["start_node"]), None)
        end_node = next((n for n in nodes if n["id"] == rel["end_node"]), None)
        if not start_node or not end_node:
            continue

        # Get label-specific unique ID fields
        def _get_uid(props, labels):
            if "StandardDocument" in labels:
                return "standard_id", props.get("standard_id")
            if "Chapter" in labels:
                return "chapter_id", props.get("chapter_id")
            if "Clause" in labels:
                return "clause_id", props.get("clause_id")
            if "Term" in labels:
                return "term_id", props.get("term_id")
            if "Requirement" in labels:
                return "requirement_id", props.get("requirement_id")
            if "Indicator" in labels:
                return "indicator_id", props.get("indicator_id")
            if "Method" in labels:
                return "method_id", props.get("method_id")
            if "StandardObject" in labels:
                return "object_id", props.get("object_id")
            return None, None
        s_field, s_val = _get_uid(start_node["properties"], set(start_node["labels"]))
        e_field, e_val = _get_uid(end_node["properties"], set(end_node["labels"]))
        if not s_field or not e_field:
            continue

        s_label = ":".join(start_node["labels"])
        e_label = ":".join(end_node["labels"])
        lines.append(f"MATCH (a:{s_label} {{{s_field}: {json.dumps(s_val, ensure_ascii=False)}}})")
        lines.append(f"MATCH (b:{e_label} {{{e_field}: {json.dumps(e_val, ensure_ascii=False)}}})")
        lines.append(f"MERGE (a)-[:{rtype}]->(b);")
        lines.append("")

    return "\n".join(lines)


